let _uid = 0;
function seg(roadName, coordinates, level, currentSpeed, freeFlowSpeed) {
  _uid++;
  const congestionPct = Math.round((1 - currentSpeed / freeFlowSpeed) * 100);
  const levelLabel = { 1: 'Free Flow', 2: 'Slow', 3: 'Congested', 4: 'Standstill' }[level] ?? 'Unknown';
  return {
    type: 'Feature',
    geometry: { type: 'LineString', coordinates },
    properties: { id: `seg-${_uid}`, roadName, level, levelLabel, currentSpeed, freeFlowSpeed, congestionPct },
  };
}

